SELECT КУРСЫ.Номер_курса, КУРСЫ.Код_преподавателя, КУРСЫ.Предмет
FROM КУРСЫ FULL OUTER JOIN ПРЕПОДАВАТЕЛИ
ON КУРСЫ.Код_преподавателя = ПРЕПОДАВАТЕЛИ.Код_преподавателя
WHERE ПРЕПОДАВАТЕЛИ.Имя IS NULL

--5.2

SELECT ПРЕПОДАВАТЕЛИ.Фамилия_преподавателя, ПРЕПОДАВАТЕЛИ.Имя, ПРЕПОДАВАТЕЛИ.Стаж,ПРЕПОДАВАТЕЛИ.Телефон 
FROM КУРСЫ FULL OUTER JOIN ПРЕПОДАВАТЕЛИ
ON КУРСЫ.Код_преподавателя = ПРЕПОДАВАТЕЛИ.Код_преподавателя
WHERE ПРЕПОДАВАТЕЛИ.Имя IS NOT NULL

